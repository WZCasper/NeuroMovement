"""Общая конфигурация pytest.

Принудительно включаем offscreen-платформу Qt, чтобы UI-тесты могли
конструировать реальные PySide6-виджеты в среде без физического дисплея
(CI-серверы, headless-контейнеры и т.п.), не теряя при этом реальность
проверки — виджеты создаются, обрабатывают события и рендерят кадры,
просто без вывода на физический экран."""
from __future__ import annotations

import os

os.environ.setdefault("QT_QPA_PLATFORM", "offscreen")
